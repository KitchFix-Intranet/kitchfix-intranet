export default function ContactList({ contacts }) {
  if (!contacts || contacts.length === 0) {
    return (
      <div className="td-contact-list td-contact-empty">
        No contacts on file.
      </div>
    );
  }

  return (
    <div className="td-contact-list">
      {contacts.map((c, i) => (
        <div key={i} className="td-contact-item">
          <div className="td-contact-avatar">{getInitials(c.name)}</div>
          <div className="td-contact-details">
            <div className="td-contact-name">{c.name || 'TBD'}</div>
            <div className="td-contact-role">{c.role}</div>
          </div>
          <div className="td-contact-actions">
            {c.email && (
              <a
                href={`https://mail.google.com/mail/?view=cm&to=${c.email}`}
                target="_blank"
                rel="noreferrer"
                className="td-mini-btn"
                title={`Email ${c.name}`}
              >
                <EmailIcon />
              </a>
            )}
            {c.phone && (
              <a
                href={`tel:${c.phone}`}
                className="td-mini-btn"
                title={`Call ${c.name}`}
              >
                <PhoneIcon />
              </a>
            )}
            {c.slackId && (
              <a
                href={`slack://user?team=T00000000&id=${c.slackId}`}
                className="td-mini-btn"
                title={`Slack ${c.name}`}
              >
                <SlackIcon />
              </a>
            )}
          </div>
        </div>
      ))}
    </div>
  );
}